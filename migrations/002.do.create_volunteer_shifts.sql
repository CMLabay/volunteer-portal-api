CREATE TABLE races (
    id INTEGER PRIMARY KEY GENERATED BY DEFAULT AS IDENTITY,
    name TEXT NOT NULL
);

CREATE TABLE shifts (
    id INTEGER PRIMARY KEY GENERATED BY DEFAULT AS IDENTITY,
    name TEXT NOT NULL,
    race_name TEXT,
    day TEXT,
    race_id INTEGER REFERENCES races(id) ON DELETE SET NULL,
    date DATE NOT NULL,
    time TEXT NOT NULL,
    user_id INTEGER REFERENCES users(id) ON DELETE SET NULL
);